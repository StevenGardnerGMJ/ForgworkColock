import UIKit
// 对颜色扩展
extension UIColor {
    
    /// 计时
    static var accentColor: UIColor {
        return UIColor(red: 92/255, green: 184/255, blue: 92/255, alpha: 1)
    }
    
    /// 休息计时
    static var breakColor: UIColor {
        return UIColor(red: 240/255, green: 90/255, blue: 90/255, alpha: 1)
    }
    
}




